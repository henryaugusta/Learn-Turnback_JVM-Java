package _13_Operasi_Perbandingan;

public class OperasiPerbandingan {
    public static void main(String[] args) {
        System.out.println("Operasi Perbandingan");

        int a = 100;
        int b = 50;

        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);
        System.out.println(a==b);
        System.out.println(a!=b);

    }
}
