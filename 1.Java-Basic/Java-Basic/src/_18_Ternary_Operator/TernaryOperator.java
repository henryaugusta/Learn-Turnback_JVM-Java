package _18_Ternary_Operator;

public class TernaryOperator {
    public static void main(String[] args) {
        var nilai = 70;

        if (nilai > 75) {
            System.out.println("Kamu Lulus");
        } else if (nilai < 50) {
            System.out.println("Kamu Tidak Lulus Karena Absen Jelek");
        }
    }
}
