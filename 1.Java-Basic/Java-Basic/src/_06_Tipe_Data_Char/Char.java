package _06_Tipe_Data_Char;

public class Char {
    public static void main(String[] args) {
        char r = 'R';
        char a = 'A';
        char z = 'Z';
        char k = 'K';
        char y = 'Y';

        System.out.print(r);
        System.out.print(a);
        System.out.print(z);
        System.out.print(k);
        System.out.print(y);
        System.out.println();

        char[] razky = {'R','A','Z','K','Y'};
        System.out.println(String.valueOf(razky));
        System.out.println((razky));

    }
}
